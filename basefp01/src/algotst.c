/*
*  Function :  GetTestData
*
*  Builds the table of input data which represents the 'tan( x )'
*  input parameter.  This is the "real world" data stream which
*  drives the algorithm.
*
*/    

n_int
GetTestData( n_void )
{        
    n_int i ; 

	inpVariable = (varsize*)
        th_malloc( ( NUM_TESTS + 1 ) * sizeof( varsize ) + sizeof (varsize)) ;
    inpVariableFree = inpVariable;
	inpVariable = ((varsize *)ROUNDUP(inpVariable, sizeof (varsize)));

    if( inpVariable == NULL )
    {
        return false ; 
    }

    /*  Copy the test values from ROM to RAM  */    
    for( i = 0 ; i < NUM_TESTS ; i++ )
    {
        inpVariable[i] = (varsize)inpVariableROM[i] ; 
    }

    return true ; 

} /* End of function 'GetTestData' */

/*
*  Function :  GetInputValues
*
*  On each pass of the table lookup, a value must be input for 'inputNum'.
*  Each time this function is called, the next input value is
*  pulled from the table in RAM. The table wraps around, so that input data is
*  continuous. A flag is returned TRUE whenever the table wraps around.
*
*/    

n_int
GetInputValues( n_void )
{        
    inputNum = inpVariable[tableCount] ; 

    /* If you run out of test data, then start table over */    
    if( ++tableCount < NUM_TESTS )
    {
        return false ; 
    }

    tableCount = 0 ; 

    return true ; 
} /* End of function 'GetInputValues' */
