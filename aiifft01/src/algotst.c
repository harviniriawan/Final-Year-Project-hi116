/*
*  Function :  GetTestData
*
*  Builds the table of input data which represents the sampled
*  input signal.  This is the "real world" data stream which
*  drives the algorithm.
*
*/    

n_int
GetTestData( n_void )
{        
    n_int i ; 

    inpSignal = (varsize*)
        th_malloc( ( NUM_TESTS + 1 ) * sizeof( varsize ) ) ; 

    if( inpSignal == NULL )
    {
        return false ; 
    }

    /*  Copy the test values from ROM to RAM  */    
    for( i = 0 ; i < NUM_TESTS ; i++ )
    {
        inpSignal[i] = inpSignalROM[i] ; 
    }

    return true ; 

} /* End of function 'GetTestData' */

/*
*  Function :  GetInputValues
*
*  On each pass of the table lookup, a value must be input for 'realData'
*  and 'imagData'.  Each time this function is called, the next input values are
*  pulled from the table in RAM.  The table wraps around, so that input data is
*  continuous.     A flag is returned TRUE whenever the table wraps around.
*
*/    

n_int
GetInputValues( varsize *real, varsize *imag )
{        
    n_int i ; 

    /* Fill the real and imaginary sample with test data */    
    for( i = 0 ; i < NUM_POINTS ; i++ )
    {        
        *( real + i ) = inpSignal[tableCount++ ] ; 
        *( imag + i ) = inpSignal[tableCount++ ] ; 
    }

    /* If you run out of test data, then start table over */    
    if( tableCount < NUM_TESTS )
    {
        return 0 ;
    }

    tableCount = 0 ; 

    return 1 ; 
    
} /* End of function 'GetInputValues' */
