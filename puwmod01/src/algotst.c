/*
*  Function :  GetTestData
*
*    Builds the table of input data which represents 'command position'
*    input parameter.  This is the "real world" data stream which
*    drives the algorithm.  This data is installed in RAM prior to execution of the
*    algorithm.
*
*/    

n_int
GetTestData( n_void )
{        
    int i ; 

    inpCmdPosition = (varsize*)th_malloc( ( NUM_TESTS + 1 ) * sizeof( varsize ) ) ; 

    if( inpCmdPosition == NULL )
    {
        return false ; 
    }

    /*  Copy the test values from ROM to RAM  */    
    for( i = 0 ; i < NUM_TESTS ; i++ )
    {
        inpCmdPosition[i] = inpCmdROM[i] ; 
    }

    return true ; 
} /* End of function 'GetTestData' */

/*
*  Function :  GetInputValues
*
*    On each pass of the table lookup, a value must be input for 'commandPos'.
*    Each time this function is called, the next input value is
*    pulled from the table in RAM.  The table wraps around, so that input data is
*    continuous.     A flag is returned TRUE whenever the table wraps around.
*
*/    

n_int
GetInputValues( n_void )
{        
    commandPos = inpCmdPosition[tableCount] ; 

    if( ++tableCount < NUM_TESTS )
    {
        return false ; 
    }

    tableCount = 0 ; 

    return true ; 
} /* End of function 'GetInputValues' */


